package softeng.commandPattern;

public class Device {
    private boolean power;
    private String name;

    public void setPower(boolean power) {
        this.power = power;
    }
    public boolean getPower() {
        return this.power;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return this.name;
    }
}
